fn main() {
    let num1 = 10;
    let num2 = 2;
    let mut result: i32;

    // Perform addition
    result = num1 + num2;
    println!("Sum: {}", result);

    // Perform subtraction
    result = num1 - num2;
    println!("Difference: {}", result);

    // Perform multiplication
    result = num1 * num2;
    println!("Product: {}", result);

    // Perform division
    result = num1 / num2;
    println!("Quotient: {}", result);

    // Perform modulus operation
    result = num1 % num2;
    println!("Remainder: {}", result);
}
